package com.mycompany.study.ch15.first.tr;

public class Member {
	
	public String name;
	public String id;
	public String password;
	public String tel;
	public String email;
	
	Member(String name, String id, String password, String tel, String email) {
		this.name = name;
		this.id = id;
		this.password = password;
		this.tel = tel;
		this.email = email;
	}
	
}
